#include <unistd.h>
#include <stdio.h>

int	occ_a(char *str)
{
	int	n;
	int	i;

	n = 0;
	i = 0;
	while (str[i])
	{
		if (str[i] == 'A')
			n++;
		i++;
	}
	return (n);
}
